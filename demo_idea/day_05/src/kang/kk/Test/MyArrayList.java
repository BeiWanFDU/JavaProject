package kang.kk.Test;

import java.util.Arrays;

public class MyArrayList<E> {
    Object[] obj;
    int size;

    public boolean add(E e) {
        obj[size] = e;
        size++;
        return true;
    }

    public E get(int index) {
        return (E) obj[index];
    }

    @Override
    public String toString() {
        return Arrays.toString(obj);
    }
}
